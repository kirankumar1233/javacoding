package dummy;

public class PrintingStatements {

	public static void main(String[] args) {
		System.out.println(10+10);  // int + int
		System.out.println('k'+'r');    // char + char
		System.out.println("kir"+"an");   // string + string
		System.out.println(10+'k');          // int + char
		System.out.println(10+"kiran");       // int + string
		//System.out.println('a'+true);       // char + boolean
		System.out.println("kiran"+true);     // string + boolean
		//System.out.println(10+false);       // int + boolean
		//System.out.println(true+false);     // boolean + boolean
	}

}
