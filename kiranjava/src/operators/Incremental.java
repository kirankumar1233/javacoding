package operators;

public class Incremental {
	public static void main(String[] args) {
		int a=11;
		int b=55;
		System.out.println(a++);//internal storage
		System.out.println(b--);
		System.out.println(a+b++);
		System.out.println(a);
		System.out.println(++a);
		System.out.println(--a);
		System.out.println(a);
		
	}

}
