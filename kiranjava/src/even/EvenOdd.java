public class EvenOdd {

	public static void main(String[] args) {
		int sum =0;
		for(int a=1; a<=100; a++)
		{
			if(a%2==0)
		
		{
		sum = sum+a;
		}
		else
		{
			sum = sum+a;
			
		}
		}
		System.out.println(sum+"even numbers sum");
		System.out.println(sum+"odd numbers sum");
	
	}

}

